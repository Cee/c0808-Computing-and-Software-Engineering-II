package po;

public class SystemState extends PO{
	int select;
	int byselect;
	int plan;
	public SystemState(int select,int bySelect, int plan){
		this.select = select;
		this.byselect = bySelect;
		this.plan = plan;
	}
	public int getByselect() {
		return byselect;
	}
	public int getPlan() {
		return plan;
	}
	public int getSelect() {
		return select;
	}
	public void setByselect(int byselect) {
		this.byselect = byselect;
	}
	public void setPlan(int plan) {
		this.plan = plan;
	}
	public void setSelect(int select) {
		this.select = select;
	}
}
